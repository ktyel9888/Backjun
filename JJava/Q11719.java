package JJava;

import java.util.Scanner;

public class Q11719 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        String str = "";
        while(sc.hasNext()){
            str = sc.nextLine();
            System.out.println(str);
        }
        //버퍼 리더를 사용해서 다시 해결해볼것.

    }

}
